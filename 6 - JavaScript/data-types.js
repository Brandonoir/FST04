// strings or textual data : "", ''
let firstName = "Brandon";
console.log("First Name:", firstName);
console.log("Type of data:", typeof firstName);

let age = 30;
console.log("My age is:", age);
console.log("Type of data:", typeof age);

//boolean: True or False
let student = true;
console.log("Am I a student?", student);
console.log("data type:", typeof student);

// null: intentional absence of a value
let car = null;
console.log("Car:", car);
console.log("data type:", typeof car);

// undefined or unintentional abscense of value
let city;
console.log("City:", city);
console.log("data type:", typeof city);
